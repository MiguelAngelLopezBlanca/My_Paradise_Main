class Modelo {
  String nombre;
  String descripcion;
  String nombreArchivo;
  String nombreImagen;

  Modelo(String name, String descripcion, String nombreArchivo,
      String nombreImagen) {
    this.nombre = name;
    this.descripcion = descripcion;
    this.nombreArchivo = nombreArchivo;
    this.nombreImagen = nombreImagen;
  }
}
